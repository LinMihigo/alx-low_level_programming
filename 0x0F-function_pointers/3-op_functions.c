/**
* op_add - returns sum
* @a: num1
* @b: num2
* Return: 0
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - returns sub of a with b
* @a: num1
* @b: num2
* Return: 0
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - returns multiple of a n b
* @a: num1
* @b: num2
* Return: a * b
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - returns a / b
* @a: num1
* @b: num2
* Return: a / b
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - returns a % b
* @a: num1
* @b: num2
* Return: modulo of a n b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
